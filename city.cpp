#include "city.h"


city::city() {
	this->city_id = 0;
	this->x_coord = 0;
	this->y_coord = 0;
	visited = false;
}
city::city(int input_id, int input_x, int input_y) {
	this->city_id = input_id;
	this->x_coord = input_x;
	this->y_coord = input_y;
	this->visited = false;
}

city::~city() {
}

void city::set_city_id(int num){
	this->city_id = num;
}
void city::set_x_coord(int num) {
	this->x_coord = num;
}
void city::set_y_coord(int num) {
	this->y_coord = num;
}
void city::set_visited(bool val) {
	this->visited = val;
}
int city::get_city_id() {
	return this->city_id;
}
int city::get_x_coord() {
	return this->x_coord;
}
int city::get_y_coord() {
	return this->y_coord;
}
bool city::get_visited() {
	return this->visited;
}
